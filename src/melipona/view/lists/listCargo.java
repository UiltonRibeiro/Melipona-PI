    private void preencherLista(List<Cargo> Cargos){
        String columns[] = {"ID","nome", "CPF"};
        String dados[][] = new String[Cargos.size()][columns.length];
        int i=0;
        
        for (Cargo cargo : Cargos) {
            dados[i] = new String[]{
                String.valueOf(cargo.getId()),
                cargo.getNome(),
                NumberFormat.getCurrencyInstance().format(cargo.getSalario())
            };
            i++;
        }
        DefaultTableModel model = new DefaultTableModel(dados,columns);
        CargosTable.setModel(model);
        
    }
